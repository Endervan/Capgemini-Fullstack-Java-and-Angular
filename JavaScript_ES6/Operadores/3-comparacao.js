// Igual (==)
// retorna verdadeiro caso os operadores sejam iguais
"3" == var1
3 == '3'

// Nao Igual (!=)
// retorna verdadeiro caso os operadores nao sejam iguais
var2 != "3"

// Estritamente  Igual (===)
// retorna verdadeiro caso os operadores  sejam iguais e msm tipo
3 === var1

// Estritamente  nao Igual (!==)
// retorna verdadeiro caso os operadores  nao  sejam iguais e nao  msm tipo
3 !== var1

// Maior que (>)
// retorna verdadeiro caso os operadores  da esquerda   sejam maior que a direita
var2 > var1
"12" > 2

// Maior que ou igual  (>=)
// retorna verdadeiro caso os operadores  da esquerda   sejam maior que a direita
var2 >= var1
"12" >= 2


// Maior que (<)
// retorna verdadeiro caso os operadores  da esquerda   sejam menor que a direita
var2 < var1
"12" < 2


// Menor  que ou igual(<=)
// retorna verdadeiro caso os operadores  da esquerda   sejam menor ou igual  a direita
var2 <= var1
"12" <= 2




